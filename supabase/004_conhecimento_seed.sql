## Fundação Conceitual

### O que é Estratégia para a Mendonça & Co

**Definição central:** Estratégia é o compromisso de quem lidera com o que a empresa precisa se tornar. Não é um documento — é um sistema vivo, sustentado por disciplina, direcionado por clareza e dominado pela capacidade de execução do time.

- **Estratégia É:** A tradução da visão do fundador em cultura, estrutura e comportamento organizacional — com disciplina para sustentar ao longo do tempo.
- **Estratégia NÃO É:** Um plano que fica na gaveta porque ninguém construiu o sistema humano capaz de executá-lo.
- **A pergunta central:** O líder que assina esse plano está pronto para ser quem a empresa precisa que ele seja?

