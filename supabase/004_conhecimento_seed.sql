### Ferramenta 2 — Diagnóstico de Alinhamento de Sócios
Aplicado separadamente com cada sócio. Revela gaps de visão antes que destruam o processo.

1. **Visão:** Onde você quer que a empresa esteja em 5 anos?
2. **Papéis:** Qual é o seu papel hoje? Qual deveria ser?
3. **Valores:** O que você nunca abriria mão na forma como a empresa opera?
4. **Expectativas:** Qual é a sua expectativa de retorno? De crescimento? De saída?

> **Sinal de alerta:** se dois sócios divergem nas dimensões Visão e Valores, o problema não é estratégico — é societário. O processo precisa resolver isso antes de qualquer plano.

