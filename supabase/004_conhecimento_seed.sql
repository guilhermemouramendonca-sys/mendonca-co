*Entrega: Visão e diferencial competitivo*

**Passo 5: Código de Cultura**
Construção ou revisão do código de cultura: valores que geram comportamento, não valores decorativos. Cultura como sistema operacional da estratégia.
*Entrega: Código de cultura*

