## Framework: Estratégias por Natureza de Mercado

### Monopólio (1 player)
- Barreiras de entrada altas, player determina preço, produtos essenciais à cadeia de valor
- **Estratégias:** Aumento de capacidade produtiva, novos mercados, aumento de preços, redução de custos, proteção de mercado

### Oligopólio (2 a 5 players)
- Formado a partir do processo de consolidação dos players do setor
- **Estratégias:** Guerra de Preços (arriscado), Minimização de Distorções, Busca por Parcerias

### Competição Monopolística (+5 players)
- Diferenciação é possível, vantagens exclusivas mas imitáveis por um período
- **Estratégias:** Guerra de Padrões (First Mover, Complementores), Inovação (jogo de longo prazo)

### Competição Perfeita
- Mercados maduros, margens baixas, barreiras de entrada baixas
- **Estratégias:** Ganhos de Participação (novos mercados, F&A), Ganhos de Eficiência (redução de custo, escala)

