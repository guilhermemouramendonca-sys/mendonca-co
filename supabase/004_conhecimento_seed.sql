### 12 Passos — 4 Blocos — 6 a 18 Meses

### Bloco 1 — Diagnóstico 360 (Passos 1 a 3)

**Passo 1: Diagnóstico Externo e Interno**
Avaliação simultânea de fora e de dentro: mercado, concorrência, posicionamento, estratégia declarada versus estratégia real, modelo de gestão, processos, marketing, funil de vendas e resultados financeiros.
