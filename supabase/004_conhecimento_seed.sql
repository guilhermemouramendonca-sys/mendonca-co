## A Tese Central

> O maior obstáculo de uma empresa de dono é o próprio dono. Não o mercado, não a concorrência, não a falta de capital. O dono que ainda não se transformou para liderar o próximo estágio é o gargalo. O trabalho mais importante que um empresário pode fazer não é no negócio — é em si mesmo. Antes de dominar o mercado, domine a si mesmo.

