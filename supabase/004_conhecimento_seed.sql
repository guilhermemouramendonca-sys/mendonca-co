## Fundação Conceitual

### O que é Estratégia (e o que não é)

**Definição central:** Estratégia é como você vai jogar para vencer — não é uma lista de tarefas.

- **Estratégia É:** Escolhas deliberadas sobre onde competir, como vencer e o que NÃO fazer.
- **Estratégia NÃO É:** Um plano de tarefas, um orçamento ou uma lista de projetos.

### As 4 Perguntas da Estratégia

1. **A quem servir?** Qual cliente irá chorar a minha ausência se a empresa fechar?
2. **Como vencer?** Qual é sua vantagem? Proposta de valor única + vantagem operacional ou comercial?
3. **Onde jogar?** Qual segmento, região, ticket, cliente-alvo você vai focar? Onde você pode vencer?
4. **O que NÃO fazer?** Quais oportunidades você vai rejeitar? Renúncias claras definem sua estratégia.

### Base Teórica: Porter + Barney

> A estratégia é escolher ações e foco para ter o melhor desempenho possível, dado sua realidade e o mercado onde atua.

- **Visão de fora — Michael Porter:** Competidores, Clientes, Fornecedores, Regulação, Novos entrantes, Substitutos.
- **Visão de dentro — Jay Barney:** Metas e valores, Recursos e habilidades, Estrutura e sistemas.
- **Equação central:** Mercado (fora) + Empresa (dentro) = Desempenho

